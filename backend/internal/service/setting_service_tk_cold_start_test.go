// TestColdStart_Defaults_LockUSDAndTrialKeyName proves the design v3 contract:
//   - bonus = $1.00 USD
//   - auto-key name = "trial" (NOT "default")
//   - all 5 toggles default to ON
func TestColdStart_Defaults_LockUSDAndTrialKeyName(t *testing.T) {
	defaults := map[string]string{}
	tkMergeDefaultColdStartSettings(defaults)

	require.Equal(t, "true", defaults[SettingKeySignupBonusEnabled],
		"signup bonus default must be ON; design v3 §2 P0-B")
	require.Equal(t, "1.00000000", defaults[SettingKeySignupBonusBalance],
		"signup bonus default must be $1.00 USD; design v3 §0 + §6")
	require.Equal(t, "true", defaults[SettingKeyAutoGenerateDefaultToken],
		"auto-issue trial key default must be ON; design v3 §2 P0-C")
	require.Equal(t, "trial", defaults[SettingKeyAutoGenerateDefaultTokenName],
		"auto-key default name must be 'trial', NOT 'default'; design v3 §0 revision")
	require.Equal(t, "true", defaults[SettingKeyPricingCatalogPublic],
		"public pricing catalog default must be ON; design v3 §2 P0-A")
}

// TestColdStart_ParseSettings_FallbackOnMissingRows proves the read path
// fails-closed-toward-on so a fresh DB still issues bonus + trial key.
func TestColdStart_ParseSettings_FallbackOnMissingRows(t *testing.T) {
	result := &SystemSettings{}
	tkApplyColdStartParsed(map[string]string{}, result)

	require.True(t, result.SignupBonusEnabled, "missing row -> default ON")
	require.InDelta(t, 1.00, result.SignupBonusBalance, 1e-9, "missing row -> $1.00")
	require.True(t, result.AutoGenerateDefaultToken, "missing row -> default ON")
	require.Equal(t, "trial", result.AutoGenerateDefaultTokenName, "missing row -> 'trial'")
	require.True(t, result.PricingCatalogPublic, "missing row -> default ON")
}

// TestColdStart_ParseSettings_ExplicitFalseTurnsOff proves admin can disable.
func TestColdStart_ParseSettings_ExplicitFalseTurnsOff(t *testing.T) {
	result := &SystemSettings{}
	tkApplyColdStartParsed(map[string]string{
		SettingKeySignupBonusEnabled:           "false",
		SettingKeySignupBonusBalance:           "5.50",
		SettingKeyAutoGenerateDefaultToken:     "false",
		SettingKeyAutoGenerateDefaultTokenName: "free-tier",
		SettingKeyPricingCatalogPublic:         "false",
	}, result)

	require.False(t, result.SignupBonusEnabled)
	require.InDelta(t, 5.50, result.SignupBonusBalance, 1e-9,
		"admin-configured balance must be honored")
	require.False(t, result.AutoGenerateDefaultToken)
	require.Equal(t, "free-tier", result.AutoGenerateDefaultTokenName,
		"admin-configured key name must be honored (US-029 AC-008)")
	require.False(t, result.PricingCatalogPublic)
}

// TestColdStart_ParseSettings_NegativeBalanceFallsBackToDefault prevents
// admin typo from creating negative-balance accounts.
func TestColdStart_ParseSettings_NegativeBalanceFallsBackToDefault(t *testing.T) {
	result := &SystemSettings{}
	tkApplyColdStartParsed(map[string]string{
		SettingKeySignupBonusBalance: "-3.14",
	}, result)
	require.InDelta(t, 1.00, result.SignupBonusBalance, 1e-9,
		"negative balance must fall back to safe default, not poison new users")
}

// TestColdStart_AppendUpdates_ClampsNegativeBalance ensures the write path
// also defends against negative input from a misbehaving admin frontend.
func TestColdStart_AppendUpdates_ClampsNegativeBalance(t *testing.T) {
	updates := map[string]string{}
	settings := &SystemSettings{
		SignupBonusEnabled:           true,
		SignupBonusBalance:           -5.0,
		AutoGenerateDefaultToken:     true,
		AutoGenerateDefaultTokenName: "",
		PricingCatalogPublic:         true,
	}
	(&SettingService{}).tkAppendColdStartSettingUpdates(updates, settings)

	require.Equal(t, "0.00000000", updates[SettingKeySignupBonusBalance],
		"negative balance must be clamped to 0 before persisting")
	require.Equal(t, "trial", updates[SettingKeyAutoGenerateDefaultTokenName],
		"empty key name must fall back to 'trial' before persisting")
}

