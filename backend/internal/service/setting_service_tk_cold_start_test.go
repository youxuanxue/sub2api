// csRepoStub is a minimal Setting repository stub used only by the cold-start
// unit tests. It mirrors the bmRepoStub pattern in setting_service_backend_mode_test.go
// but stays in this file so the cold-start spec is self-contained.
type csRepoStub struct {
	values    map[string]string
	getErrFor map[string]error
	getCalls  map[string]int
	updates   map[string]string
}

func newCSRepoStub() *csRepoStub {
	return &csRepoStub{
		values:    map[string]string{},
		getErrFor: map[string]error{},
		getCalls:  map[string]int{},
	}
}

func (s *csRepoStub) Get(ctx context.Context, key string) (*Setting, error) {
	panic("unexpected Get call")
}

func (s *csRepoStub) GetValue(ctx context.Context, key string) (string, error) {
	s.getCalls[key]++
	if err := s.getErrFor[key]; err != nil {
		return "", err
	}
	v, ok := s.values[key]
	if !ok {
		return "", ErrSettingNotFound
	}
	return v, nil
}

func (s *csRepoStub) Set(ctx context.Context, key, value string) error {
	panic("unexpected Set call")
}

func (s *csRepoStub) GetMultiple(ctx context.Context, keys []string) (map[string]string, error) {
	panic("unexpected GetMultiple call")
}

func (s *csRepoStub) SetMultiple(ctx context.Context, settings map[string]string) error {
	if s.updates == nil {
		s.updates = make(map[string]string, len(settings))
	}
	for k, v := range settings {
		s.updates[k] = v
	}
	return nil
}

func (s *csRepoStub) GetAll(ctx context.Context) (map[string]string, error) {
	panic("unexpected GetAll call")
}

func (s *csRepoStub) Delete(ctx context.Context, key string) error {
	panic("unexpected Delete call")
}

