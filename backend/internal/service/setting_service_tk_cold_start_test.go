func TestComputeSignupBonus_HonorsEnabledFlag(t *testing.T) {
	repo := newCSRepoStub()
	repo.values[SettingKeySignupBonusEnabled] = "false"
	repo.values[SettingKeySignupBonusBalance] = "10.00"
	svc := NewSettingService(repo, &config.Config{})

	got := svc.ComputeSignupBonus(context.Background())
	require.InDelta(t, 0.0, got, 1e-9,
		"disabled flag must short-circuit even when balance > 0")
}

