package service

import (
	"context"
	"strconv"
)

// TokenKey: new-user cold-start settings are isolated from setting_service.go
// to keep upstream merges small.
//
// Wiring (mirrors the new-api bridge pattern in setting_service_tk_bridge_passkey_payments.go):
//   - tkMergeDefaultColdStartSettings   ← invoked from InitializeDefaultSettings
//   - tkApplyColdStartParsed            ← invoked from parseSettings
//   - tkAppendColdStartSettingUpdates   ← invoked from UpdateSystemSettings
//
// All five settings are documented in docs/approved/user-cold-start.md.
//
// Defaults match the approved design v3:
//
//	signup_bonus_enabled              = true
//	signup_bonus_balance              = 1.00          (USD, same unit as users.balance)
//	auto_generate_default_token       = true
//	auto_generate_default_token_name  = "trial"      (NOT "default" — see US-029)
//	pricing_catalog_public            = true
const (
	defaultSignupBonusBalanceUSD   = 1.00
	defaultAutoGenerateDefaultName = "trial"
)

func tkMergeDefaultColdStartSettings(defaults map[string]string) {
	defaults[SettingKeySignupBonusEnabled] = "true"
	defaults[SettingKeySignupBonusBalance] = strconv.FormatFloat(defaultSignupBonusBalanceUSD, 'f', 8, 64)
	defaults[SettingKeyAutoGenerateDefaultToken] = "true"
	defaults[SettingKeyAutoGenerateDefaultTokenName] = defaultAutoGenerateDefaultName
	defaults[SettingKeyPricingCatalogPublic] = "true"
}

func tkApplyColdStartParsed(settings map[string]string, result *SystemSettings) {
	// Booleans default to true if the row is missing or non-"false".
	result.SignupBonusEnabled = !isFalseSettingValue(settings[SettingKeySignupBonusEnabled])
	result.AutoGenerateDefaultToken = !isFalseSettingValue(settings[SettingKeyAutoGenerateDefaultToken])
	result.PricingCatalogPublic = !isFalseSettingValue(settings[SettingKeyPricingCatalogPublic])

	if v, err := strconv.ParseFloat(settings[SettingKeySignupBonusBalance], 64); err == nil && v >= 0 {
		result.SignupBonusBalance = v
	} else {
		result.SignupBonusBalance = defaultSignupBonusBalanceUSD
	}

	name := settings[SettingKeyAutoGenerateDefaultTokenName]
	if name == "" {
		name = defaultAutoGenerateDefaultName
	}
	result.AutoGenerateDefaultTokenName = name
}

func (s *SettingService) tkAppendColdStartSettingUpdates(updates map[string]string, settings *SystemSettings) {
	updates[SettingKeySignupBonusEnabled] = strconv.FormatBool(settings.SignupBonusEnabled)
	if settings.SignupBonusBalance < 0 {
		settings.SignupBonusBalance = 0
	}
	updates[SettingKeySignupBonusBalance] = strconv.FormatFloat(settings.SignupBonusBalance, 'f', 8, 64)
	updates[SettingKeyAutoGenerateDefaultToken] = strconv.FormatBool(settings.AutoGenerateDefaultToken)

	name := settings.AutoGenerateDefaultTokenName
	if name == "" {
		name = defaultAutoGenerateDefaultName
	}
	updates[SettingKeyAutoGenerateDefaultTokenName] = name

	updates[SettingKeyPricingCatalogPublic] = strconv.FormatBool(settings.PricingCatalogPublic)
}

// ---------------------------------------------------------------------------
// Read-side Getters (single source of truth for runtime callers).
//
// Auth / API-key / public-pricing call sites MUST go through these helpers
// instead of reading raw setting values, so the "missing row defaults to ON"
// semantics stays consistent and so unit tests can stub one method.
// ---------------------------------------------------------------------------

// IsSignupBonusEnabled returns true unless the admin explicitly stored "false".
// Missing / corrupt rows fail-closed-toward-on so a fresh DB still gives bonus.
func (s *SettingService) IsSignupBonusEnabled(ctx context.Context) bool {
	if s == nil || s.settingRepo == nil {
		return true
	}
	value, err := s.settingRepo.GetValue(ctx, SettingKeySignupBonusEnabled)
	if err != nil {
		return true
	}
	return !isFalseSettingValue(value)
}

// GetSignupBonusBalance returns the configured signup bonus in USD.
// Returns 0 (not the hard-coded default) when the setting is missing/invalid,
// because zero is a meaningful "off" signal in computeSignupBonus.
func (s *SettingService) GetSignupBonusBalance(ctx context.Context) float64 {
	if s == nil || s.settingRepo == nil {
		return defaultSignupBonusBalanceUSD
	}
	value, err := s.settingRepo.GetValue(ctx, SettingKeySignupBonusBalance)
	if err != nil {
		return defaultSignupBonusBalanceUSD
	}
	v, perr := strconv.ParseFloat(value, 64)
	if perr != nil || v < 0 {
		return defaultSignupBonusBalanceUSD
	}
	return v
}

// ComputeSignupBonus returns the actual USD amount to credit on a registration,
// honoring both the enabled flag and the balance value. Result is always >= 0.
// This is the ONE function the auth service should call.
func (s *SettingService) ComputeSignupBonus(ctx context.Context) float64 {
	if !s.IsSignupBonusEnabled(ctx) {
		return 0
	}
	v := s.GetSignupBonusBalance(ctx)
	if v < 0 {
		return 0
	}
	return v
}

// IsAutoGenerateDefaultTokenEnabled returns true unless the admin explicitly
// stored "false". Missing rows default to ON so a fresh deployment still
// auto-issues a trial key.
func (s *SettingService) IsAutoGenerateDefaultTokenEnabled(ctx context.Context) bool {
	if s == nil || s.settingRepo == nil {
		return true
	}
	value, err := s.settingRepo.GetValue(ctx, SettingKeyAutoGenerateDefaultToken)
	if err != nil {
		return true
	}
	return !isFalseSettingValue(value)
}

// GetAutoGenerateDefaultTokenName returns the configured key name (default "trial").
// Empty / missing rows fall back to "trial" so the auto-key flow always has a name.
func (s *SettingService) GetAutoGenerateDefaultTokenName(ctx context.Context) string {
	if s == nil || s.settingRepo == nil {
		return defaultAutoGenerateDefaultName
	}
	value, err := s.settingRepo.GetValue(ctx, SettingKeyAutoGenerateDefaultTokenName)
	if err != nil || value == "" {
		return defaultAutoGenerateDefaultName
	}
	return value
}

// IsPricingCatalogPublic gates the public /api/v1/public/pricing endpoint.
// True (default) means "anonymous users can see the catalog".
func (s *SettingService) IsPricingCatalogPublic(ctx context.Context) bool {
	if s == nil || s.settingRepo == nil {
		return true
	}
	value, err := s.settingRepo.GetValue(ctx, SettingKeyPricingCatalogPublic)
	if err != nil {
		return true
	}
	return !isFalseSettingValue(value)
}
