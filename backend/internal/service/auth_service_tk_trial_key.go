package service

import (
	"context"

	"github.com/Wei-Shaw/sub2api/internal/pkg/logger"
)

// TokenKey: auto-issued first API key ("trial") on successful registration.
// Isolated from auth_service.go so upstream merges only touch the call sites
// (one line per register path) and the rest of the policy lives here.
//
// Wiring (US-029):
//   - TrialKeyIssuer interface          ← injected via SetTrialKeyIssuer(...)
//   - apiKeyTrialIssuer                  ← default impl wrapping APIKeyService
//   - issueTrialKeyIfEnabled             ← invoked best-effort post-Create
//
// Defaults and admin overrides live in setting_service_tk_cold_start.go
// (auto_generate_default_token = true, auto_generate_default_token_name = "trial").
//
// Why an interface instead of just calling APIKeyService directly:
//   1. AuthService is constructed before APIKeyService in some test fixtures —
//      a setter avoids a circular constructor dependency.
//   2. Unit tests for the bonus flow can pass a noop / mock issuer without
//      pulling in the entire APIKeyService graph (groups / cache / cfg).
//   3. Future extensions (issue trial subscription, attach trial group, etc.)
//      can plug a richer issuer here without touching auth_service.go again.

// TrialKeyIssuer issues the auto-generated first API key for newly registered
// users. Implementations MUST be best-effort: they may log on failure but must
// NOT return an error that propagates back to the user — registration already
// succeeded by the time this is called.
type TrialKeyIssuer interface {
	IssueTrialKeyIfEnabled(ctx context.Context, userID int64)
}

// SetTrialKeyIssuer wires the trial-key issuer post-construction.
// Safe to call with nil; in that case auth flows skip auto key creation.
func (s *AuthService) SetTrialKeyIssuer(issuer TrialKeyIssuer) {
	if s == nil {
		return
	}
	s.trialKeyIssuer = issuer
}

// issueTrialKeyIfEnabled is the call-site shim used inside register paths.
// It guards against a missing issuer (DI race in tests) and never panics.
func (s *AuthService) issueTrialKeyIfEnabled(ctx context.Context, userID int64) {
	if s == nil || s.trialKeyIssuer == nil {
		return
	}
	s.trialKeyIssuer.IssueTrialKeyIfEnabled(ctx, userID)
}

// ---------------------------------------------------------------------------
// Default implementation: backed by APIKeyService + SettingService.
// ---------------------------------------------------------------------------

// apiKeyTrialIssuer is the production TrialKeyIssuer. It reads the toggle and
// the configured key name from SettingService, then calls APIKeyService.Create
// with sensible defaults (no group, no quota cap, no expiry, no IP rules).
type apiKeyTrialIssuer struct {
	apiKeyService  *APIKeyService
	settingService *SettingService
}

// NewTrialKeyIssuer constructs the production issuer. Both deps are required;
// returns nil if either is nil so callers can detect a misconfigured wire.
func NewTrialKeyIssuer(api *APIKeyService, settings *SettingService) TrialKeyIssuer {
	if api == nil || settings == nil {
		return nil
	}
	return &apiKeyTrialIssuer{
		apiKeyService:  api,
		settingService: settings,
	}
}

// IssueTrialKeyIfEnabled honors the admin toggle and key-name setting.
// All failures are logged at WARN-equivalent and swallowed — registration
// should never appear to fail because the auto key flow couldn't run.
func (i *apiKeyTrialIssuer) IssueTrialKeyIfEnabled(ctx context.Context, userID int64) {
	if i == nil || i.apiKeyService == nil || i.settingService == nil {
		return
	}
	if !i.settingService.IsAutoGenerateDefaultTokenEnabled(ctx) {
		return
	}

	name := i.settingService.GetAutoGenerateDefaultTokenName(ctx)
	// Final paranoia guard: even if the setting row is corrupted, we never
	// want to materialize an unnamed key.
	if name == "" {
		name = "trial"
	}

	req := CreateAPIKeyRequest{
		Name: name,
		// All quota / IP / expiry fields default to zero-value, which the
		// downstream service interprets as "unlimited" / "no restriction".
		// The user can tighten these later from the dashboard.
	}

	if _, err := i.apiKeyService.Create(ctx, userID, req); err != nil {
		// Best-effort: log + move on. We deliberately do NOT bubble this
		// error to the user — the registration already returned 200.
		logger.LegacyPrintf(
			"service.auth",
			"[Auth] auto_trial_key_issue_failed userID=%d name=%s err=%v",
			userID, name, err,
		)
		return
	}

	logger.LegacyPrintf(
		"service.auth",
		"[Auth] auto_trial_key_issued userID=%d name=%s",
		userID, name,
	)
}
