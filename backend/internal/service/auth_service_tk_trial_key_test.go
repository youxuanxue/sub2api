func TestUS030_NewTrialKeyIssuer_NilDeps_ReturnsNil(t *testing.T) {
	require.Nil(t, NewTrialKeyIssuer(nil, nil))
	require.Nil(t, NewTrialKeyIssuer(nil, &SettingService{}))
	require.Nil(t, NewTrialKeyIssuer(&APIKeyService{}, nil))
}

func TestUS030_ApiKeyTrialIssuer_DisabledSetting_NoCall(t *testing.T) {
	// AC: when auto_generate_default_token=false, the issuer must short-circuit
	// before reaching APIKeyService.Create. We pass an APIKeyService with a nil
	// userRepo; if the issuer ever reached Create, the test would panic at
	// the first userRepo.GetByID call, so the absence of a panic is the
	// behavior assertion.
	settings := NewSettingService(&settingRepoStub{values: map[string]string{
		SettingKeyAutoGenerateDefaultToken: "false",
	}}, &config.Config{})

	issuer := &apiKeyTrialIssuer{
		apiKeyService:  &APIKeyService{}, // would panic on Create — must NOT be reached
		settingService: settings,
	}
	require.NotPanics(t, func() {
		issuer.IssueTrialKeyIfEnabled(context.Background(), 1)
	})
}

func TestUS030_ApiKeyTrialIssuer_NilSelf_NoPanic(t *testing.T) {
	var issuer *apiKeyTrialIssuer
	require.NotPanics(t, func() {
		issuer.IssueTrialKeyIfEnabled(context.Background(), 1)
	})
}

func TestUS030_ApiKeyTrialIssuer_DefaultName_Trial(t *testing.T) {
	// US-030 explicit naming requirement: when no admin override is set, the
	// configured key name MUST be "trial" (not "default", not empty). We
	// verify GetAutoGenerateDefaultTokenName fallback through the issuer's
	// path by checking it directly on SettingService.
	settings := NewSettingService(&settingRepoStub{values: map[string]string{}}, &config.Config{})
	require.Equal(t, "trial", settings.GetAutoGenerateDefaultTokenName(context.Background()),
		"missing setting row must default to 'trial', NOT 'default' or empty")
}
